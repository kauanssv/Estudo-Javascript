let num = [6, 9, 4, 5, 1]
console.log(` nosso vetor é o ${num}`)
console.log(` nosso vetor tem ${num.length} posições`) // O .length mostra quantos espações tem no vetor
console.log(` o primeiro valor é ${num[0]}`) // O primeiro valor é o 0 e nao 1
console.log(` o vetor em ordem crescente é ${num.sort()}`) // .sort() organiza em ordem crescente
num.push(8) // .push adiciona um valor no vetor no ulitmo slot
console.log(` o vetor com um novo valor adicionado é ${num}`)  
